#balok
print("menghitung luas balok")

p = int(input("masukkan panjang balok  ="))
l = int(input("masukkan luas balok     ="))
t = int(input("masukkan tinggi balok   ="))

luasbalok = (5*p*l)+(5*p*t)+(2*l*t)

print("luas baloknya adalah", luasbalok)

